        fun firstPuzzle() {
            println("Result: " + getTreesCount(1, 3))
        }

        private fun getTreesCount(downStep: Int, rightStep: Int): Int {
            var treesCount = 0
            var index = 0

            for (i in lines.size - 1 downTo 0 step downStep) {
                val line = lines[lines.size - 1 - i]
                if (line[index] == '#') {
                    treesCount++
                }
                index += rightStep

                if (index >= line.length) {
                    index -= line.length
                }
            }
            return treesCount
        }

        fun secondPuzzle() {
            val versionOneCount = getTreesCount(1, 1)
            val versionTwoCount = getTreesCount(1, 3)
            val versionThreeCount = getTreesCount(1, 5)
            val versionFourCount = getTreesCount(1, 7)
            val versionFiveCount = getTreesCount(2, 1)
            val result: Long = versionOneCount.toLong() * versionTwoCount * versionThreeCount *
                    versionFourCount * versionFiveCount

            println("Version one: $versionOneCount")
            println("Version two (242): $versionTwoCount")
            println("Version three: $versionThreeCount")
            println("Version four: $versionFourCount")
            println("Version five: $versionFiveCount")
            println("Result: $result")
        }
